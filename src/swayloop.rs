#[derive(Debug)]
pub enum SwayWindowSelect {
    Waiting,
    Select,
    Finish,
}

type WindowInfo = (i32, i32, i32, i32);

pub static CAN_EXIT: Lazy<Arc<Mutex<SwayWindowSelect>>> =
    Lazy::new(|| Arc::new(Mutex::new(SwayWindowSelect::Waiting)));
pub static FINAL_WINDOW: Lazy<Arc<Mutex<WindowInfo>>> =
    Lazy::new(|| Arc::new(Mutex::new((0, 0, 0, 0))));

pub fn get_window() -> thread::JoinHandle<()> {
    thread::spawn(|| {
        let subs = [swayipc::EventType::Window];

        let mut geometry = (0, 0, 0, 0);
        for event in swayipc::Connection::new().unwrap().subscribe(subs).unwrap() {
            if let Ok(mut can_exit) = CAN_EXIT.lock() {
                if let SwayWindowSelect::Select = *can_exit {
                    *can_exit = SwayWindowSelect::Finish;
                    break;
                }
            }
            if let Ok(swayipc::Event::Window(window)) = event {
                let rect = window.container.rect;
                geometry = (rect.x, rect.y, rect.width, rect.height);
                tracing::info!(
                    "window on x: {}, y: {}, width: {}, height: {}",
                    rect.x,
                    rect.y,
                    rect.width,
                    rect.height
                );
            }
        }
        let mut final_window = FINAL_WINDOW.lock().unwrap();
        *final_window = geometry;
    })
}

pub fn swaylayer() {
    // All Wayland apps start by connecting the compositor (server).
    let conn = Connection::connect_to_env().unwrap();

    // Enumerate the list of globals to get the protocols the server implements.
    let (globals, mut event_queue) = registry_queue_init(&conn).unwrap();
    let qh = event_queue.handle();

    // The compositor (not to be confused with the server which is commonly called the compositor) allows
    // configuring surfaces to be presented.
    let compositor = CompositorState::bind(&globals, &qh).expect("wl_compositor is not available");
    // This app uses the wlr layer shell, which may not be available with every compositor.
    let layer_shell = LayerShell::bind(&globals, &qh).expect("layer shell is not available");
    // Since we are not using the GPU in this example, we use wl_shm to allow software rendering to a buffer
    // we share with the compositor process.
    let shm = Shm::bind(&globals, &qh).expect("wl_shm is not available");

    // A layer surface is created from a surface.
    let surface = compositor.create_surface(&qh);

    // And then we create the layer shell.
    let layer =
        layer_shell.create_layer_surface(&qh, surface, Layer::Top, Some("simple_layer"), None);
    // Configure the layer surface, providing things like the anchor on screen, desired size and the keyboard
    // interactivity
    layer.set_anchor(Anchor::TOP);
    layer.set_keyboard_interactivity(KeyboardInteractivity::Exclusive);
    layer.set_size(256, 256);

    // In order for the layer surface to be mapped, we need to perform an initial commit with no attached\
    // buffer. For more info, see WaylandSurface::commit
    //
    // The compositor will respond with an initial configure that we can then use to present to the layer
    // surface with the correct options.
    layer.commit();

    // We don't know how large the window will be yet, so lets assume the minimum size we suggested for the
    // initial memory allocation.
    let pool = SlotPool::new(256 * 256 * 4, &shm).expect("Failed to create pool");

    let mut simple_layer = SimpleLayer {
        // Seats and outputs may be hotplugged at runtime, therefore we need to setup a registry state to
        // listen for seats and outputs.
        registry_state: RegistryState::new(&globals),
        seat_state: SeatState::new(&globals, &qh),
        output_state: OutputState::new(&globals, &qh),
        shm,

        exit: false,
        first_configure: true,
        pool,
        width: 256,
        height: 256,
        layer,
        keyboard: None,
        keyboard_focus: false,
    };

    // We don't draw immediately, the configure will notify us when to first draw.
    loop {
        event_queue.blocking_dispatch(&mut simple_layer).unwrap();

        if simple_layer.exit {
            tracing::info!("exiting layer_shell");
            break;
        }
    }
    let mut can_exit = CAN_EXIT.lock().unwrap();
    *can_exit = SwayWindowSelect::Select;
}

struct SimpleLayer {
    registry_state: RegistryState,
    seat_state: SeatState,
    output_state: OutputState,
    shm: Shm,

    exit: bool,
    first_configure: bool,
    pool: SlotPool,
    width: u32,
    height: u32,
    layer: LayerSurface,
    keyboard: Option<wl_keyboard::WlKeyboard>,
    keyboard_focus: bool,
}

impl CompositorHandler for SimpleLayer {
    fn scale_factor_changed(
        &mut self,
        _conn: &Connection,
        _qh: &QueueHandle<Self>,
        _surface: &wl_surface::WlSurface,
        _new_factor: i32,
    ) {
        // Not needed for this example.
    }

    fn frame(
        &mut self,
        _conn: &Connection,
        qh: &QueueHandle<Self>,
        _surface: &wl_surface::WlSurface,
        _time: u32,
    ) {
        self.draw(qh);
    }

    fn transform_changed(
        &mut self,
        _conn: &Connection,
        _qh: &QueueHandle<Self>,
        _surface: &wl_surface::WlSurface,
        _new_transform: wl_output::Transform,
    ) {
    }
}

